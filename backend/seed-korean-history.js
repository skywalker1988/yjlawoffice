async function seed() {
  console.log(`🇰🇷 한국사 히스토리 시드 시작 — ${events.length}개 이벤트`);

  let inserted = 0;
  let skipped = 0;

  for (const ev of events) {
    try {
      // Check for duplicates by title
      const existing = await db
        .select()
        .from(historyEvents)
        .where(require("drizzle-orm").eq(historyEvents.title, ev.title))
        .limit(1);

      if (existing.length > 0) {
        skipped++;
        continue;
      }

      await db.insert(historyEvents).values({
        title: ev.title,
        description: ev.description,
        year: ev.year,
        month: ev.month || null,
        day: ev.day || null,
        endYear: ev.endYear || null,
        category: ev.category,
        region: ev.region || null,
        country: ev.country || null,
        importance: ev.importance || 3,
        latitude: ev.latitude || null,
        longitude: ev.longitude || null,
        source: ev.source || null,
      });
      inserted++;
    } catch (err) {
      console.error(`  ❌ ${ev.title}: ${err.message}`);
    }
  }

  console.log(`✅ 완료: ${inserted}개 삽입, ${skipped}개 중복 건너뜀`);
}

